class Book {
    constructor(title, description, writer, dateOfWritting, chapters, normalSR, ChronologicalSR) {
        this.title = title;
        this.description = description;
        this.writer = writer;
        this.dateOfWritting = dateOfWritting;
        this.chapters = chapters;
        this.normalSR = normalSR;
        this.ChronologicalSR = ChronologicalSR;
    }
}